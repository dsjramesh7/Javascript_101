let a = 12;

//if
if (a > 7) {
  console.log("yes a is greater then 7");
}

// if else
if (a < 7) {
  console.log("yes a is less then 7");
} else {
  console.log("no a is greater then 7 buddy else case here");
}

//if else if
if (a < 7) {
  console.log("yes a is less then 7");
} else if (a === 12) {
  console.log("a is equal buddy else if case here");
} else {
  console.log("no a is greater then 7 buddy else case here");
}
